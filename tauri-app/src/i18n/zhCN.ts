export function formatSyncProgress(
  kind: SyncKind,
  scannedPages: number,
  insertedRows: number,
) {
  const kindText = kind === "full" ? "全量" : "增量";

  return `正在${kindText}同步：第 ${scannedPages} 页，新增 ${insertedRows} 条`;
}
